    private void organizationComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_organizationComboBoxActionPerformed
        // TODO add your handling code here:
        Organization org = (Organization) organizationComboBox.getSelectedItem();
        if (org != null) {
            populateEmployee(org);
            populateRole(org);
        }

    }//GEN-LAST:event_organizationComboBoxActionPerformed

    private void createUserAccountJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createUserAccountJButtonActionPerformed
        // TODO add your handling code here:
        String username = usernameJTextField.getText();
        String password = passwordJTextField.getText();

        Employee employee = (Employee) employeeJComboBox.getSelectedItem();
        Role role = (Role) roleJComboBox.getSelectedItem();
        Organization org = (Organization) organizationComboBox.getSelectedItem();

        org.getUserAccountDirectory().createUserAccount(username, password, employee, role);
        populateTable();
    }//GEN-LAST:event_createUserAccountJButtonActionPerformed

    public void populateEmployee(Organization org) {
        employeeJComboBox.removeAllItems();
        for (Employee emp : org.getEmployeeDirectory().getEmpList()) {
            employeeJComboBox.addItem(emp);
        }
    }

    public void populateRole(Organization org) {
        roleJComboBox.removeAllItems();
        for (Role role : org.getSupportedRole()) {
            roleJComboBox.addItem(role);
        }
    }

