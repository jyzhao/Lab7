    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) userAccountJTable.getModel();
        dtm.setRowCount(0);
        for (Organization org : business.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : org.getUserAccountDirectory().getUserAccountList()) {
                Object[] row = new Object[2];
                row[0] = ua.getUserName();
                row[1] = ua.getRole();
                dtm.addRow(row);
            }
        }
    }

    public void populateOrgComboBox() {
        organizationComboBox.removeAllItems();
        for (Organization org : business.getOrganizationDirectory().getOrganizationList()) {
            organizationComboBox.addItem(org);
        }
    }

